// Regenerate evals/COVERAGE.md from the multi-agent inventory sweep.
//   bun evals/_inventory/build-coverage-md.ts
import { readFileSync, writeFileSync } from "node:fs";

type Feature = { id: string; name: string; area: string; kind: string; summary: string; docPointer?: string; keyApi?: string };
type Task = { id: string; area: string; kind: string; tier: string; verify: string };

const F: Feature[] = JSON.parse(readFileSync("evals/_inventory/coverage-features.json", "utf8"));
const T: Task[] = JSON.parse(readFileSync("evals/_inventory/coverage-tasks.json", "utf8"));

const isUndoc = (d?: string) => String(d ?? "").toUpperCase().includes("UNDOCUMENTED");
const groupBy = <X,>(arr: X[], k: (x: X) => string): Record<string, X[]> =>
  arr.reduce((m: Record<string, X[]>, x) => ((m[k(x)] = m[k(x)] ?? []).push(x), m), {});

const fByArea = groupBy(F, (x) => x.area || "unknown");
const tByArea = groupBy(T, (x) => x.area || "unknown");
const areas = [...new Set([...Object.keys(fByArea), ...Object.keys(tByArea)])].sort();
const undoc = F.filter((x) => isUndoc(x.docPointer));

const md: string[] = [];
md.push("# Smithers Coverage Map", "");
md.push("> Generated by `evals/_inventory/build-coverage-md.ts` from a multi-agent sweep of the codebase.");
md.push(`> ${F.length} features across ${areas.length} areas · ${T.length} candidate eval tasks · ${undoc.length} features flagged UNDOCUMENTED.`);
md.push("> Backbone for wave generation: every feature should be reachable by at least one eval.", "");

md.push("## Tasks by area", "");
md.push("| Area | Features | Tasks | knowledge | authoring | ops | db-query | weak | sota |");
md.push("| --- | --- | --- | --- | --- | --- | --- | --- | --- |");
for (const a of areas) {
  const ts = tByArea[a] ?? [];
  const c = (k: string) => ts.filter((t) => t.kind === k).length;
  const tier = (t: string) => ts.filter((x) => x.tier === t).length;
  md.push(`| ${a} | ${(fByArea[a] ?? []).length} | ${ts.length} | ${c("knowledge")} | ${c("authoring")} | ${c("ops")} | ${c("db-query")} | ${tier("weak")} | ${tier("sota")} |`);
}
md.push("");

md.push(`## ⚠️ Features flagged UNDOCUMENTED (${undoc.length})`, "");
md.push("Immediate docs-gap candidates — an eval that fails here points straight at a doc to write.", "");
for (const x of undoc) md.push(`- \`${x.keyApi || x.name}\` — ${x.summary} _(area: ${x.area})_`);
md.push("");

md.push("## Features by area", "");
for (const a of areas) {
  md.push(`### ${a}`, "");
  for (const x of (fByArea[a] ?? []).slice().sort((p, q) => (p.name > q.name ? 1 : -1))) {
    const doc = isUndoc(x.docPointer) ? "**UNDOCUMENTED**" : x.docPointer || "?";
    md.push(`- **${x.name}** (\`${x.keyApi || "-"}\`) — ${x.summary} · _${doc}_`);
  }
  md.push("");
}

writeFileSync("evals/COVERAGE.md", md.join("\n") + "\n");
console.log(`wrote evals/COVERAGE.md (${md.length} lines)`);
